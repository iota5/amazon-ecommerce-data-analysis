-- Q20: Determine the top 3 product categories that have shown the highest increase in sales over the past year compared to the previous year. (SQL)
with cte as(
select ProductCategory,
sum(case when year(OrderDate)=2019 then SalePrice end) as Rev_2019,
sum(case when year(OrderDate)=2020 then SalePrice end) as Rev_2020
from orders
where Status='Delivered'
group by ProductCategory
)

select *, Rev_2020-Rev_2019 as Change_in_Rev
from cte
order by Change_in_Rev desc
limit 3;
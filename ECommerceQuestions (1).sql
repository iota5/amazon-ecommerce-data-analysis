-- Q9: Which products have seen an increase or decrease in sales over the years? 
with cte as(
select Product, year(OrderDate) as order_year, sum(SalePrice) as revenue
from orders
group by Product, year(OrderDate)
),
cte2 as(
select *,
revenue- lag(revenue) over(partition by Product order by order_year asc) as YoY_difference,
case
when revenue- lag(revenue) over(partition by Product order by order_year asc) <0 then 'Decrease'
when revenue- lag(revenue) over(partition by Product order by order_year asc) =0 then 'Same'
when revenue- lag(revenue) over(partition by Product order by order_year asc) >0 then 'Increase'
else null
end as YoY_Trend,
row_number() over(partition by Product order by order_year asc) as rn

from cte
order by Product, order_year asc)

select *,
case 
when (select revenue from cte2 where Product=c.Product and rn= (select max(rn) from cte2 where Product=c.Product)) < (select revenue from cte2 where Product=c.Product and rn=1) then 'Net Decrease'
when (select revenue from cte2 where Product=c.Product and rn= (select max(rn) from cte2 where Product=c.Product)) = (select revenue from cte2 where Product=c.Product and rn=1) then 'Net Neutral'
when (select revenue from cte2 where Product=c.Product and rn= (select max(rn) from cte2 where Product=c.Product)) > (select revenue from cte2 where Product=c.Product and rn=1) then 'Net Increase'
end as Net_Change
from cte2 c;


-- Q14: Identify the top 5 most valuable customers using a composite score that combines three key metrics: (SQL)
-- Total Revenue (50% weight): The total amount of money spent by the customer.
-- Order Frequency (30% weight): The number of orders placed by the customer, indicating their loyalty and engagement.
-- Average Order Value (20% weight): The average value of each order placed by the customer, reflecting the typical transaction size.

with customer_metrics as(
select CustomerID, round(sum(SalePrice),2) as total_revenue, count(distinct OrderID) as frequency, round(avg(SalePrice),2) as avg_order_value
from orders
where Status= 'Delivered'
group by CustomerID
),
normalized as(
    SELECT *,
        PERCENT_RANK() OVER (ORDER BY total_revenue) AS revenue_score,
        PERCENT_RANK() OVER (ORDER BY frequency) AS frequency_score,
        PERCENT_RANK() OVER (ORDER BY avg_order_value) AS aov_score
    FROM customer_metrics
    )
SELECT 
    CustomerID,
    total_revenue,
    frequency,
    avg_order_value,
    ROUND(
        0.5 * revenue_score +
        0.3 * frequency_score +
        0.2 * aov_score
    , 4) AS composite_score
FROM normalized
ORDER BY composite_score DESC
LIMIT 5;


-- Q15: Calculate the month-over-month growth rate in total revenue across the entire dataset. (SQL)
with cte as(
select DATE_FORMAT(OrderDate, '%Y-%m') as Year_Month_period, sum(SalePrice) as Total_Revenue
from orders o
group by DATE_FORMAT(OrderDate, '%Y-%m')
)
select * , 
round(((Total_Revenue-lag(Total_Revenue) over(order by Year_Month_period))*100.0/lag(Total_Revenue) over(order by Year_Month_period)),2) as MoM_Growth_rate
from cte ;

-- Q16: Calculate the rolling 3-month average revenue for each product category. (SQL)
with cte as(
select ProductCategory, date_format(OrderDate,'%Y-%m') as Year_month_period, sum(SalePrice) as Revenue
from orders
where Status= 'Delivered'
group by ProductCategory, date_format(OrderDate,'%Y-%m')
)

select *, 
round(avg(Revenue) over(partition by ProductCategory order by Year_month_period rows between 2 preceding and current row),2) as three_month_rolling_avg
from cte;

